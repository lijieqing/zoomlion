package J1939;

//定义指示灯状态结构
public class J1939_lamp_ts 
{
	byte lampState_u8;						// 指示灯开关状态，每2位对应一个指示灯，从低到高依次为保护灯、琥珀色警告灯、红色停止灯、故障指示灯
											//		00 -- 关闭   01 -- 打开 
	byte lampFlash_u8;						// 指示灯闪烁状态，每2位对应一个指示灯，从低到高依次为保护灯、琥珀色警告灯、红色停止灯、故障指示灯
											//		00 -- 慢闪	  01 -- 快闪  11 -- 不闪烁
	public J1939_lamp_ts() {
		super();
	}

} 
